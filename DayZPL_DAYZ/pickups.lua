function manualRelootPoint (point)
	--outputDebugString("RELOOT")
	--triggerClientEvent(getRootElement(), "playMyMusic", getRootElement())
	if isElement ( point ) then
		local lootPlace = getElementData ( point, "parent" ) 
		if lootPlace and itemTable[lootPlace] then
			for i, item in ipairs(itemTable[lootPlace]) do
				local value = math.percentChance (item[5],math.random(0.1,1))
				setElementData(point,item[1],value)
				if weaponToAmmo[item[1]] and value > 0 then
					setElementData(point,weaponToAmmo[item[1]],math.random(1,1))
				end
			end
			refreshItemLoot (point,lootPlace)
		end
	end
end


