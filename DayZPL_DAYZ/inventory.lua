function getPedClothesID(thePed, clothingSlot)
	if not isElement(thePed) or type(clothingSlot) ~= "number" then
		error("Invalid arguments to setPedClothes()!", 2)
	end
	local texture, model = getPedClothes(thePed, clothingSlot)
	if not texture then
		return false
	else
		local _, id = getTypeIndexFromClothes(texture, model)
		return id
	end
end

function getClothBodySlot (cloth)
	for cat_name, cloth_table in pairs ( clothSlots ) do 
		for i, v in pairs (cloth_table) do
			if i == cloth then
				return cat_name
			end
		end
	end
	return false
end

function useRuckSack(name)
	if sacks[name] then
		local current_slots = getElementData(getLocalPlayer(),"MAX_Slots") or 4
		local sack_slots = sacks[name].slots
		if current_slots < sack_slots then
			setElementData(getLocalPlayer(),"MAX_Slots",sack_slots)
			refreshInventory()
		else
			local using_slots = getPlayerCurrentSlots()
			if using_slots-4 > 0 then
				triggerEvent("displayClientInfo",getLocalPlayer(),"Inventory","Выложите вещи для замены рюкзака (еще "..tostring(using_slots-4).." slot).",255,0,0)
			else 
				setElementData(getLocalPlayer(),"MAX_Slots",sack_slots)
				refreshInventory()
			end
		end
	end
end
