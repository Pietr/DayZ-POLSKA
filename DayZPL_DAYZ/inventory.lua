			guiSetVisible(RIGHTUP2,not guiGetVisible(RIGHTUP2))
			guiSetVisible(LEFTTUP2,not guiGetVisible(LEFTTUP2))
			guiSetVisible(DOWN2,not guiGetVisible(DOWN2))
			guiSetVisible(RIGHTDOWN2,not guiGetVisible(RIGHTDOWN2))
			guiSetVisible(LEFTDOWN2,not guiGetVisible(LEFTDOWN2))				
			showCursor(not isCursorShowing())
			refreshInventory()
			if guiGetVisible(inventoryWindows) == true and
			guiGetVisible(UP) == true and
			guiGetVisible(RIGHTUP) == true and
			guiGetVisible(LEFTTUP) == true and
			guiGetVisible(DOWN) == true and
			guiGetVisible(RIGHTDOWN) == true and
			guiGetVisible(LEFTDOWN) == true and
			guiGetVisible(InfoWin) == true and
			guiGetVisible(UP2) == true and
			guiGetVisible(RIGHTUP2) == true and
			guiGetVisible(LEFTTUP2) == true and
			guiGetVisible(DOWN2) == true and
			guiGetVisible(RIGHTDOWN2) == true and
			guiGetVisible(LEFTDOWN2) == true then
				onClientOpenInventoryStopMenu ()
			else
				hideRightClickInventoryMenu ()
				if isPlayerInLoot() then
					if getElementData ( isPlayerInLoot(), "itemloot" ) then
						triggerServerEvent ("startUpdatingLoot",isPlayerInLoot())
					end
				end
			end
			if isPlayerInLoot() then
				local col = getElementData(getLocalPlayer(),"currentCol")
				local gearName = getElementData(getLocalPlayer(),"lootname")
				refreshLoot(col,gearName)
			end
		end
	end	
