function createZombie()
	local totalzombie = 0
	for _, data in ipairs(tableZombie) do
		spawnNewZombie(data[1],data[2],data[3],nil,data[4],data[5])
		totalzombie = totalzombie + 1
	end
	outputDebugString("Map "..tostring(totalzombie).." zombies,animals,boss,bots")
end
createZombie()

function destroyDeadZombie (ped,pedCol)
	destroyElement(ped)
	destroyElement(pedCol)
end

zombieKilled = function(killer, headshot)
	if killer then
		setElementData(killer, "zombieskilled", getElementData(killer, "zombieskilled") + 1)
	end
	local skin = getElementModel(source)
	local x, y, z = getElementPosition(source)
	local ped = createPed(skin, x, y, z)
	local pedCol = createColSphere(x, y, z, 1.5)
	killPed(ped)
	setTimer(destroyDeadZombie,3600000, 1, ped, pedCol)
	attachElements(pedCol, ped, 0, 0, 0)
	setElementData(pedCol, "parent", ped)
	setElementData(pedCol, "playername", "Zombie")
	setElementData(pedCol, "deadman", true)
	setElementData(ped, "deadzombie", true)
	setElementData(pedCol, "deadman", true)
	local creator = getElementData ( source, "creator" ) 
	setTimer ( function () 
		if isElement ( creator ) then
			setElementData ( creator, "spawnedzombies", ( getElementData ( creator, "spawnedzombies" ) or 1 ) - 1 )
		end
	end, 3600000, 1 )
	
	local spawn_x = getElementData(source,"spawn_x")
	local spawn_y = getElementData(source,"spawn_y")
	local spawn_z = getElementData(source,"spawn_z")
	local spawn_ztype = getElementData(source,"spawn_ztype")
	local spawn_skin = getElementData(source,"spawn_skin")
	
	setTimer(spawnZombieKill,3600000,1,spawn_x,spawn_y,spawn_z,spawn_ztype,spawn_skin)
	
	
	local time = getRealTime()
	local hours = time.hour
	local minutes = time.minute
	setElementData(pedCol,"deadreason","Wygląda na to, że ten zombie padł. Orientacyjna godzina śmierci: "..hours..":"..minutes..".")
	if getElementData ( source, "soldier" ) then
		for i,item in ipairs(itemTableSoldZombies) do
			setElementData(pedCol, item[1], math.random(0,item[5]))
		end
	elseif getElementData ( source, "fast" ) then
		for i,item in ipairs(itemTableFastZombies) do
			setElementData(pedCol, item[1], math.random(0,item[5]))
		end
	--Твварини
		elseif getElementData ( source, "bear" ) then
		for i,item in ipairs(bearItems) do
			setElementData(pedCol, item[1], math.random(1,item[5]))
		end
		elseif getElementData ( source, "wolf" ) then
		for i,item in ipairs(wolfItems) do
			setElementData(pedCol, item[1], math.random(1,item[5]))
		end
		elseif getElementData ( source, "fox" ) then
		for i,item in ipairs(foxItems) do
			setElementData(pedCol, item[1], math.random(1,item[5]))
		end
		elseif getElementData ( source, "animal" ) then
		for i,item in ipairs(AnimalItems) do
			setElementData(pedCol, item[1], math.random(1,item[5]))
		end
		elseif getElementData ( source, "boss_blood" ) then
		for i,item in ipairs(bossBlood) do
			setElementData(pedCol, item[1], math.random(2,item[5]))
		end
		elseif getElementData ( source, "boss_all" ) then
		for i,item in ipairs(bossAll) do
			setElementData(pedCol, item[1], math.random(1,item[5]))
		end
		elseif getElementData ( source, "pila" ) then
		for i,item in ipairs(pialarama) do
			setElementData(pedCol, item[1], math.random(1,item[5]))
		end
	--
		else
		for i,item in ipairs(itemTableZombies) do
			if item[5] then
				setElementData(pedCol, item[1],math.random(0,item[5]))
			end
		end
	end
	if isElement ( getElementData(source,"parent") ) then
		destroyElement(getElementData(source,"parent"))
    end
	local attaches = getAttachedElements ( source )
	if attaches then
		for ElementKey, ElementValue in ipairs ( attaches ) do
			if isElement ( ElementValue ) then
				if not getElementData ( ElementValue, "zRadZone" ) then
					destroyElement ( ElementValue )
				end
			end
		end
	end
	if isElement ( getElementData ( source, "zombieShape" ) ) then destroyElement ( getElementData ( source, "zombieShape" ) ) end
	if isElement ( getElementData ( source, "shapeFar" ) ) then destroyElement ( getElementData ( source, "shapeFar" ) ) end
	if isElement ( getElementData ( source, "shapeNear" ) ) then destroyElement ( getElementData ( source, "shapeNear" ) ) end
	destroyElement(source)
	if headshot == true then
		setPedHeadless( ped, true )
		setElementData(killer, "headshots", getElementData(killer, "headshots") + 1)
	end
end
